.286
.MODEL SMALL
.STACK
.DATA
    LETRERO1 DB 'DAME UN NUMERO: ','$'
    LETRERO2 DB 'EL NUMERO ES: ','$'
    VALOR DB (0),'$'
    LN DB 0AH,0DH,'$'
.CODE

MAIN PROC FAR
    MOV AX,@DATA    
    MOV DS,AX     

    LEA DX, LETRERO1
    CALL WRITE
    
    CALL READ
    MOV VALOR,AL
    
    LEA DX,LN
    CALL WRITE
    
    LEA DX, LETRERO2
    CALL WRITE
    
    LEA DX,VALOR
    CALL WRITE
    
    .EXIT
MAIN ENDP

READ PROC
    MOV AH,01H
    INT 21H
    RET
READ ENDP

WRITE PROC
    MOV AH,09H    ;09H NECESITA CENTINELA
    INT 21H
    RET
WRITE ENDP

END MAIN