.286
.MODEL SMALL
WRITE MACRO LETRERO
    MOV AH, 09H
    LEA DX, LETRERO
    INT 21H
ENDM
.STACK
.DATA
    LETRERO1 DB 'DAME UN NUMERO: ','$'
    LETRERO2 DB 'EL NUMERO ES: ','$'
    VALOR DB (0),'$'
    LN DB 0AH,0DH,'$'
.CODE

MAIN PROC FAR
    MOV AX,@DATA    
    MOV DS,AX     

    WRITE LETRERO1
    CALL READ
    MOV VALOR, AL
    
    WRITE LN
    WRITE LETRERO2
    WRITE VALOR
  
    .EXIT
MAIN ENDP

READ PROC
    MOV AH,01H
    INT 21H
    RET
READ ENDP

END MAIN